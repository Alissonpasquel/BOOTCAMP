var respuesta = prompt("¿Cómo te sientes el día de hoy?")

switch (respuesta){

     case 'Feliz':
        alert("Sigue estando feliz.");
        break;

     case 'Triste':
        alert("No estés triste, todo se soluciona.");
        break;
        
     case 'Molesta':
        alert("Trata de tranquilizarte.");
        break;
        
      default:
         alert("Tu estado de ánimo es complicado.");
         break;



}
